    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        this.setInfo("Conectando...", Color.BLACK);
        
        chClient = new ChatClientManager("127.0.0.1", 12345);
        if(chClient.isConnected()){
            //Para poder arrancar el hilo de recepcion de mensajes
            this.chClient.setListener(this.mainFrame.getUserPanelListener(), new LoginJPanelListener());
            
            //enviar al server el usuario y la clave y esperar un ok para continuar  
            String xmlMensaje = "<?xml version=\"1.0\" encoding=\"UTF-8\"?>" +
                                "<message>" +
                                "<action>connect</action>" +
                                "<user><name>" + jTextFieldUser.getText().trim() +"</name></user>" +
                                "<password>" + (new String(jPasswordField.getPassword())) +"</password>" +
                                "<port_id>" + chClient.getLocalPort() +"</port_id>" +
                                "</message>"; 
            
            this.chClient.sendText(xmlMensaje);
                        
        }else{
            this.setInfo("No se pudo conectar con el servidor.", Color.RED);
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

    /**
     * Metodo que se invoca desde la clase interna LoginJPanelListener en el metodo loginAccepted(), que
     * a su vez lo dispara el servidor al llegarle al cliente un mensaje de UPDATE
     */
    private void logueado() {
        this.setInfo("", Color.BLACK);
        //mostramos el panel de usuarios
        this.mainFrame.setJPanel(1);
         //Le pasamos la conexion al Panel de usuarios, para que interactue con el servidor
        this.mainFrame.setUserPanelConnection(chClient);
        
        this.mainFrame.setUser(this.jTextFieldUser.getText().trim());
        this.logueado = true;
    } 

    public void setLogueado(boolean logueado) {
        this.logueado = logueado;
    }
    

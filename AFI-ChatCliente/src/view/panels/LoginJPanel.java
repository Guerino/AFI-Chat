    public void stopChat() {
        if(chClient != null)
             chClient.stop();
        
        this.logueado = false;
    }  
    
    class LoginJPanelListener implements ILoginJPanel {
        /**
         * Nota: el LoginJPanelListener al entrar al UserJPanel, se pone a null
         * por lo que este metodo solo se invoca desde doProcessUpdate una sola vez
         * al comienzo para loguearse
         */
        @Override
        public void loginAccepted() {       
           logueado();         
        }

        @Override
        public void setTexInfoLabel(String text) {
             setInfo(text, Color.RED);
        }
        
        @Override
        public boolean isLogueado() {
            return logueado;
        }
        
    }

